def load_torsion_from_file (filename):
    """ Function doc """
    text = open(filename, 'r')
    
    TORSIONS = {'ALA':{},
                'ARG':{},
                'ASN':{},
                'ASP':{},
                'CYS':{},
                'GLN':{},
                'GLU':{},
                'GLY':{},
                'HIS':{},
                'HSD':{},
                'HSE':{},
                'HSP':{},
                'HIE':{},
                'HID':{},
                'ILE':{},
                'LEU':{},
                'LYS':{},
                'MET':{},
                'PHE':{},
                'PRO':{},
                'CPR':{},
                'SER':{},
                'THR':{},
                'TRP':{},
                'TYR':{},
                'VAL':{}
                 }
    
    for line in text:
        try:
            line2 = line.split()
            if line2[0] == 'TORSION':
                try:
                    TORSIONS[line2[1]][line2[2]] = [line2[3],line2[4],line2[5],line2[6]]
                except:
                    pass
            
        except:
            pass
            
    #pprint (TORSIONS)
    return TORSIONS


